import { api } from "../api/api";

export default function RatingStars({ noteId }) {
    const rate = async (val) => {
        await api.post(`/rate/${noteId}`, { rating: val });
        alert("Thanks for rating ⭐");
    };

    return (
        <div>
            {[1, 2, 3, 4, 5].map((n) => (
                <span
                    key={n}
                    onClick={() => rate(n)}
                    style={{ cursor: "pointer", fontSize: "20px" }}
                >
                    ⭐
                </span>
            ))}
        </div>
    );
}
